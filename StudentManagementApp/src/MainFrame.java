    private void dozent_buttonhinzufuegenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dozent_buttonhinzufuegenActionPerformed
        // Dozent Hinzufuegen 
        try {

            did = dozent_did.getText();
            vname = dozent_vorname.getText();
            nname = dozent_nachnname.getText();
            idnummer = dozent_idnummer.getText();
            fachbereich = dozent_fachbereich.getText();
            fakultaet = dozent_fakultaet.getText();
            mail = dozent_mail.getText();
            alte = dozent_alte.getText();

            boolean exp3 = Dozent.checkIDNummer(idnummer);
            boolean exp4 = Dozent.checkDozentIDNummer(did);

            if (exp3 == true && exp4 == true) {

                Dozent dozent1 = new Dozent(vname, nname, did, idnummer, fachbereich, fakultaet, mail, alte);
                Uni.hinfuegenDozent(dozent1);

                System.out.println("Der Dozent hat hinzugefügt!");
                JOptionPane.showMessageDialog(null, "Dozent hat hinzugefügt");

            } else if (exp3 == true && exp4 == false) {
                JOptionPane.showMessageDialog(null, "Dozent IDNummer nicht passt.");
            } else if (exp3 == false && exp4 == true) {
                JOptionPane.showMessageDialog(null, "IDNummer nicht passt.");
            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Gibt es ein Problem\n" + e);

        }

        dozent_did.setText("");
        dozent_vorname.setText("");
        dozent_nachnname.setText("");
        dozent_idnummer.setText("");
        dozent_fachbereich.setText("");
        dozent_fakultaet.setText("");
        dozent_mail.setText("");
        dozent_alte.setText("");

    }//GEN-LAST:event_dozent_buttonhinzufuegenActionPerformed
    
    private void dozent_buttonsuchenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dozent_buttonsuchenActionPerformed
        // Dozent Suchen
        try {
            DefaultTableModel model = (DefaultTableModel) dozent_tablesuchen.getModel();

            int rows = model.getRowCount();
            for (int i = rows - 1; i >= 0; i--) {
                model.removeRow(i);
            }

            String dozentidnummer = dozent_textsuchen.getText();
            Dozent d = Uni.suchenDozent(dozentidnummer);

            String didnummer = d.getDozentIDNummer();
            String vorname = d.getVorname();
            String nachname = d.getNachname();
            String id = d.getIDNummer();
            String fachbereich = d.getFachBereich();
            String fakultaet = d.getFakultaet();
            String mail = d.getMail();
            String alte = d.getAlte();
            Object[] s = {didnummer, vorname, nachname, id, fachbereich, fakultaet, mail, alte};
            model.addRow(s);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Dozent nicht gefunden");
        }
        dozent_textsuchen.setText("");

    }//GEN-LAST:event_dozent_buttonsuchenActionPerformed

