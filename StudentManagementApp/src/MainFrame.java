    private void std_buttonhinzufuegenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_std_buttonhinzufuegenActionPerformed
        //Student Hinzufuegen
        try {
            mtr = std_matrikelnummer.getText();
            v = std_vorname.getText();
            n = std_nachname.getText();
            IDn = std_idnummer.getText();
            s = std_studiengang.getText();
            f = std_fakultaet.getText();
            ma = std_mail.getText();
            se = std_studiengang.getText();
            alt = std_alte.getText();

            boolean exp1 = Student.checkIDNummer(IDn);
            boolean exp2 = Student.checkMatrikelNummer(mtr);

            if (exp1 == true && exp2 == true) {

                Student student1 = new Student(v, n, mtr, IDn, s, f, ma, se, alt);
                Uni.hinfuegenStudent(student1);
                JOptionPane.showMessageDialog(null, "Student hat hinzugefuegt");

            } else if (exp1 == true && exp2 == false) {
                JOptionPane.showMessageDialog(null, "Matrikelnummer nicht passt.");
            } else if (exp1 == false && exp2 == true) {
                JOptionPane.showMessageDialog(null, "IDNummer nicht passt.");
            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Gibt es ein Problem\n" + e);

        }

        std_matrikelnummer.setText("");
        std_vorname.setText("");
        std_nachname.setText("");
        std_idnummer.setText("");
        std_studiengang.setText("");
        std_fakultaet.setText("");
        std_mail.setText("");
        std_alte.setText("");
        std_semester.setText("");
    }//GEN-LAST:event_std_buttonhinzufuegenActionPerformed
    
    private void std_buttonsuchenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_std_buttonsuchenActionPerformed
        // Student Suchen
        try {
            DefaultTableModel model = (DefaultTableModel) std_tablesuchen.getModel();
            int rows = model.getRowCount();
            for(int i = rows - 1; i >=0; i--)
            {
                model.removeRow(i);
            }
            String mtrnm = std_textsuchen.getText();
            Student a = Uni.suchenStudent(mtrnm);

            String matrikelnummer = a.getMatrikelNummer();
            String vorname = a.getVorname();
            String nachname = a.getNachname();
            String id = a.getIDNummer();
            String studiengang = a.getStudiengang();
            String fakultaet = a.getFakultaet();
            String mail = a.getMail();
            String semester = a.getSemester();
            String alte = a.getAlte();
            Object[] s = {matrikelnummer,vorname,nachname,id,studiengang,fakultaet,mail,semester,alte};
            model.addRow(s);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Student nicht gefunden");
        }

        std_textsuchen.setText("");
    }//GEN-LAST:event_std_buttonsuchenActionPerformed
    
